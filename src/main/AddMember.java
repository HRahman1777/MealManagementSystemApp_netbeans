    private void submitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitButtonActionPerformed
        String username = usernameTextField.getText();
        String contact = contactTextField.getText();
        String deposit = depositTextField.getText();

        Connection Conn = null;

        try {
            String myDriver = "org.gjt.mm.mysql.Driver";
            String myURL = "jdbc:mysql://localhost/meal_management_app";
            Class.forName(myDriver);

            Connection conn = (Connection) DriverManager.getConnection(myURL, USERNAME, "");

            String query = "insert into addmember (username, contact, deposit)values(?, ?, ?)";
            PreparedStatement ps = conn.prepareStatement(query);
            ps.setString(1, username);
            ps.setString(2, contact);
            ps.setString(3, deposit);
            ps.execute();

        } catch (Exception e) {
            System.err.println(e.getMessage());
        }

        JOptionPane.showMessageDialog(rootPane, "Added!");

    }//GEN-LAST:event_submitButtonActionPerformed

