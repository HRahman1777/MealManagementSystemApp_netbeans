    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        // TODO add your handling code here:
        String username = usernameTextField.getText();
        String contact = contactTextField.getText();
        String deposit = depositTextField.getText();

        Connection conn = null;
        try {
            String myDriver = "org.gjt.mm.mysql.Driver";
            String myURL = "jdbc:mysql://localhost/meal_management_app";
            Class.forName(myDriver);
            conn = (Connection) DriverManager.getConnection(myURL, USERNAME, "");

            String sql = "UPDATE addmember SET deposit=?, contact=? WHERE username=?";
            PreparedStatement statement = conn.prepareStatement(sql);
            statement.setString(1, deposit);
            statement.setString(2, contact);
            statement.setString(3, username);

            int rowsUpdated = statement.executeUpdate();
            if (rowsUpdated > 0) {
                //System.out.println("An existing user was updated successfully!");
                JOptionPane.showMessageDialog(rootPane, deposit+" "+contact+"\n"+"Update Successful!");
            }

            conn.close();

        } catch (Exception e) {
            System.err.println("Got an exception!");
            System.err.println(e.getMessage());
        }
    }//GEN-LAST:event_updateButtonActionPerformed

